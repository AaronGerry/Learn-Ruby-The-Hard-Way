days = "Mon Tue Wed Thu Fri Sat Sun"
months = "Jan\nFeb\nMar\nApr\nMay\nAug"

# This lists all the days in the same row
puts "Here are the days: #{days}"
# "n" indicates starting a new line on the next row
puts "Here are the months: #{months}"

# The """ makes everything in btw act as a string, on separate rows, instead of having to put "'s around each line
